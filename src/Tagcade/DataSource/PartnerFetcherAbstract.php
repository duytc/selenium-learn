<?php

namespace Tagcade\DataSource;

use Psr\Log\LoggerInterface;

abstract class PartnerFetcherAbstract {

    /**
     * @var LoggerInterface
     */
    protected $logger;

    /**
     * @return LoggerInterface
     */
    public function getLogger()
    {
        return $this->logger;
    }

    /**
     * @param LoggerInterface $logger
     */
    public function setLogger($logger)
    {
        $this->logger = $logger;
    }
}